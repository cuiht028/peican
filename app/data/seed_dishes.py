"""成渝家常菜预置库种子数据（约 80 道）。

包含 主食(~15) / 素菜(~25) / 荤菜(~25) / 汤品(~15)。每道菜含：
口味等级、适宜 / 禁忌年龄层、适宜 / 禁忌体质、每人份主材(克重+分类)、
菜谱步骤、功效、适宜 / 禁忌人群、注意事项、适宜节气。

主材名称与 ``seed_ingredients.py`` 严格对应；分类由食材库自动查表。
数据由架构侧按成渝家常体系草拟，供离线配餐，后续可由营养顾问迭代。
"""

from __future__ import annotations

from typing import Any

from app import config
from app.data.seed_ingredients import INGREDIENT_SEED

# 食材名 -> 分类，供主材自动标注 category
_CAT_MAP = {item["name"]: item["category"] for item in INGREDIENT_SEED}


def ing(name: str, grams: int) -> dict[str, Any]:
    """构造一道菜的主材条目（自动查分类）。"""
    return {"name": name, "grams": grams, "category": _CAT_MAP.get(name, 5)}


def tk(spicy: int = 2, numb: int = 2, acid: int = 1, salt: int = 2,
        sweet: int = 1) -> dict[str, int]:
    """构造口味等级字典。"""
    return {"spicy": spicy, "numb": numb, "acid": acid, "salt": salt, "sweet": sweet}


def d(
    dish_name: str,
    dish_type: int,
    region_type: int = 1,
    taste: dict | None = None,
    suit_age: list | None = None,
    forbid_age: list | None = None,
    suit_health: list | None = None,
    forbid_health: list | None = None,
    main_ingredients: list | None = None,
    recipe_steps: list | None = None,
    efficacy: str = "",
    suitable_crowd: str = "",
    taboo_crowd: str = "",
    note: str = "",
    suit_solar: list | None = None,
) -> dict[str, Any]:
    """构造一道菜的种子字典，未提供的字段取合理默认。"""
    return {
        "dish_name": dish_name,
        "dish_type": dish_type,
        "region_type": region_type,
        "taste": taste or dict(config.BASE_TASTE),
        "suit_age": suit_age if suit_age is not None else [1, 2, 3, 4, 5],
        "forbid_age": forbid_age or [],
        "suit_health": suit_health or [],
        "forbid_health": forbid_health or [],
        "main_ingredients": main_ingredients or [],
        "recipe_steps": recipe_steps or [],
        "efficacy": efficacy,
        "suitable_crowd": suitable_crowd,
        "taboo_crowd": taboo_crowd,
        "note": note,
        "suit_solar": suit_solar or [],
    }


